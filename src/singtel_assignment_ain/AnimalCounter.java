package singtel_assignment_ain;

public class AnimalCounter {
	private int walkCounter = 0;
	private int singCounter = 0;
	private int swimCounter = 0;
	private int flyCounter = 0;
	
	public int getWalkCounter() {
		return walkCounter;
	}
	public void setWalkCounter(int walkCounter) {
		this.walkCounter = walkCounter;
	}
	public int getSingCounter() {
		return singCounter;
	}
	public void setSingCounter(int singCounter) {
		this.singCounter = singCounter;
	}
	public int getSwimCounter() {
		return swimCounter;
	}
	public void setSwimCounter(int swimCounter) {
		this.swimCounter = swimCounter;
	}
	public int getFlyCounter() {
		return flyCounter;
	}
	public void setFlyCounter(int flyCounter) {
		this.flyCounter = flyCounter;
	}
	
	
}
